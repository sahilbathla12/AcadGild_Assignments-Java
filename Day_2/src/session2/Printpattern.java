package session2;

public class Printpattern {

	public static void main(String[] args) {
		int i;
		for( i=1;i<=5;i++)
		{
			for(int j=1;j<=i;j++)
			{
				System.out.print(j);
				
			}
			System.out.println();
		}
		
		for(i=4;i>=1;--i)
		{
			for(int j=1;j<=i;j++
					)
			{
				System.out.print(j);
				
			}
			System.out.println();
			
		}
		
	}
}
